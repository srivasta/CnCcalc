package CnCCalc::Table;
use Carp;

{  # scope for ultra-private meta-object for class attributes
  my %Table = ();

  for my $datum (keys %Table ) {
    no strict "refs";
    *$datum = sub {
      use strict "refs";
      my ($self, $newvalue) = @_;
      $Table{$datum} = $newvalue if @_ > 1;
      return $Table{$datum};
    }
  }
}

sub new{
  my $this = shift;
  my %params = @_;
  my $class = ref($this) || $this;
  my $self = {};

  croak("Illegal arguments") unless defined $params{'Name'};
  my $temp;
  if (defined $params{'Temporary'}) {
    $temp = $params{'Temporary'} && ! $::ConfOpts{'Keep'}
  }
  %{ $self } = (
		Name        => $params{'Name'},
		Columns     => {},
		Constraints => {},
		Temporary   => $temp,
		Initialized => 0,
		Created     => 0
	       );

  bless $self, $class;
  $self->{' _Debug'} = 0;
  return $self;
}

sub add_column (){
  my $self = shift;
  my %params = @_;

  croak("Illegal arguments") unless defined $params{'Name'};
  croak("Illegal arguments") unless defined $params{'Type'};
  if (defined $self->{'Columns'}->{$params{'Name'}} ) {
    undef $self->{'Columns'}->{$params{'Name'}}
  }
  %{ $self->{'Columns'}->{$params{'Name'}} } =
    ( 'Name' => $params{'Name'},
      'Type' => $params{'Type'},
      'NOT Null' => defined $params{'NOT Null'} ? $params{'NOT Null'} : 0,
      'Default'  => defined $params{'Default'} ? $params{'Default'} : undef,
    );
  $self->{'Initialized'}++;
  return $self;
}

sub add_constraint (){
  my $self = shift;
  my %params = @_;

  croak("Illegal arguments") unless defined $params{'Name'};
  if (defined  $self->{'Constraints'}->{$params{'Name'}} ) {
    undef $self->{'Constraints'}->{$params{'Name'}};
  }
  %{ $self->{'Constraints'}->{$params{'Name'}} } =
    (
     'Name' => $params{'Name'},
     'Body' => $params{'Body'},
    );
  return $self;
}

sub delete_column () {
  my $self = shift;
  my %params = @_;
  croak("Illegal arguments") unless defined $params{'Name'};
  undef $self->{'Columns'}->{$params{'Name'}}
    if defined $self->{'Columns'}->{$params{'Name'}};
}

sub delete_constraint () {
  my $self = shift;
  my %params = @_;
  croak("Illegal arguments") unless defined $params{'Name'};
  undef $self->{'Constraints'}->{$params{'Name'}}
    if defined $self->{'Constraints'}->{$params{'Name'}};
}

sub column_as_string () {
  my $self = shift;
  my %params = @_;

  my $col = $params{'Name'};
  my $string_rep = $self->{'Columns'}->{ $col }->{'Name'} .
    ' ' x (20 - length $self->{'Columns'}->{ $col }->{'Name'} ) .
      $self->{'Columns'}->{ $col }->{'Type'} .
	' ' x (20 - length $self->{'Columns'}->{ $col }->{'Type'} );
  if ($self->{'Columns'}->{ $col }->{'NOT Null'}) {
    $string_rep .= " NOT NULL ";
  }
  else {
    $string_rep .= "          ";
  }

  $string_rep .= " DEFAULT " . $self->{'Columns'}->{ $col }->{'Default'}
    if defined $self->{'Columns'}->{ $col }->{'Default'};
  return $string_rep;
}

sub columns_as_string () {
  my $self = shift;
  my %params = @_;
  my $string_rep = '';


  for my $col (keys %{$self->{'Columns'}}) {
    $string_rep .= ",\n" if $string_rep;
    $string_rep .= $self->column_as_string('Name' => $col);
  }
  #print STDERR $string_rep, "\n";
  return $string_rep;
}

sub constraint_as_string () {
  my $self = shift;
  my %params = @_;

  my $col = $params{'Name'};
  my $string_rep = '';

  $string_rep = $self->{'Constraints'}->{ $col }->{'Body'}
    if defined $self->{'Constraints'}->{ $col }->{'Body'};
  return $string_rep;
}

sub constraints_as_string () {
  my $self = shift;
  my %params = @_;
  my $string_rep = '';

  for my $col (keys %{$self->{'Constraints'}}) {
    $string_rep .= ",\n" if $string_rep;
    $string_rep .= $self->constraint_as_string('Name' => $col);
  }
  #print STDERR $string_rep, "\n";
  return $string_rep;
}

sub drop_table () {
  my $self = shift;
  my %params = @_;
  my $string_rep = '';

  $string_rep = "DROP TABLE " . $self->{'Name'} . ";";
  return $string_rep;
}

sub create_table () {
  my $self = shift;
  my %params = @_;
  my $string_rep = '';

  $string_rep = "CREATE ";
  $string_rep .= " TEMPORARY " if $self->{'Temporary'};
  $string_rep .= " TABLE " . $self->{'Name'} . " (\n";
  $string_rep .= $self->columns_as_string ();
  my $constraints = $self->constraints_as_string ();
  $string_rep .= ",\n" if $constraints;
  $string_rep .= $constraints;
  $string_rep .= "\n);\n";
  return $string_rep;
}

