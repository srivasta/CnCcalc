    else {
      $hashref->{$table}->{'Table Ref'} =
	CnCCalc::Table->new('Name' => $table);
    }
    for my $column (keys %{ $hashref->{$table}->{'Columns'} }) {
      print STDERR $column, "\n";
    }
  }
